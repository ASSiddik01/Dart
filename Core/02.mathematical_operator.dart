void main() {
  int a = 5;
  int b = 2;

  // Summation
  int sum = a + b;
  print('Sum = $sum');

  // Substruction
  int sub = a - b;
  print('Sub = $sub');

  // Multipication
  int multi = a * b;
  print('Multi = $multi');

  // Division
  double divi = (a / b);
  print('Div = $divi');

  // Modulus
  int modulas = a % b;
  print('Modulas = $modulas');
}
